#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*z;
	size_t			x;

	x = 0;
	z = b;
	while (x != len)
	{
		z[x] = c;
		x++;
	}
	return (z);
}
